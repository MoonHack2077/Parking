    /**
     * Constructor para que a la hora de iniciar aparezcan los datos respectivos
     * @param moto 
     */
    public Factura(Moto moto) {
        initComponents();
        setLocationRelativeTo(null);
        this.moto = moto;
        
        lblPlaca.setText(moto.getPlaca());
        lblEntrada.setText(moto.getFechaLLegada().toString());
        lblSalida.setText(moto.getFechaSalida().toString());
        lblHorasPermanecidas.setText(String.valueOf(moto.getHorasPermanecidas()));
        total = moto.getHorasPermanecidas()*valorPorHora;
        if( moto.isCasco() ){
            lblCasco.setText("+3000");
            lblTotal.setText( String.valueOf(total + 3000) );
        }else{
            lblCasco.setText("+0"); 
            lblTotal.setText( String.valueOf(total) );
        }
        
    }

